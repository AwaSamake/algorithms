/**
 *
 * 063. Unique Paths II
 *     Follow up for "Unique Paths":
 *     
 *     Now consider if some obstacles are added to the grids. How many unique paths would 
 *     there be?
 *     
 *     An obstacle and empty space is marked as 1 and 0 respectively in the grid.
 *     
 *     For example,
 *     There is one obstacle in the middle of a 3x3 grid as illustrated below.
 *     
 *     [
 *       [0,0,0],
 *       [0,1,0],
 *       [0,0,0]
 *     ]
 *     The total number of unique paths is 2.
 *     
 *     Note: m and n will be at most 100.
 *
 */
import java.util.*;

class Solution {
	public int uniquePathsWithObstacles(int[][] obstacleGrid) {
		if (obstacleGrid.length < 1 || obstacleGrid[0].length < 1 || obstacleGrid[obstacleGrid.length-1][obstacleGrid[0].length - 1] == 1) {
			return 0;
		}
		return uniquePathsWithObstacles(obstacleGrid, 0, 0);
	}
	
	public int uniquePathsWithObstacles(int[][] obstacleGrid, int r, int c) {
		if (r >= obstacleGrid.length || c >= obstacleGrid[0].length || obstacleGrid[r][c] == 1) {
			return 0;
		} else if (r == obstacleGrid.length - 1 && c == obstacleGrid[0].length - 1) {
			return 1;
		}
		
		if (obstacleGrid[r][c] == 0) {
			obstacleGrid[r][c] -= uniquePathsWithObstacles(obstacleGrid, r + 1, c);
			obstacleGrid[r][c] -= uniquePathsWithObstacles(obstacleGrid, r, c + 1);
		}
		return -obstacleGrid[r][c];
	}
}

